public class Suma {
    private int n1;
    private int n2;
    private int resultado;

    public Suma(int n1, int n2) {
        this.n1 = n1;
        this.n2 = n2;
    }
    public Suma(int n1) {
        this.n1 = n1;

    }
    public int getSuma(){
        resultado = n2 + n2;
        return resultado;
    }
    public int incrementa(){
        n1++;
        return n1;
    }
}
